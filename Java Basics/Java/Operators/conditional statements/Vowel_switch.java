class Vowel_switch{
	public static void main(String[] args){
		char ch= 'i';
	switch(ch){
		case 'a':
		case 'e':
		case 'i':
		case 'o':
		case 'u':
		case 'A':
		case 'E':
		case 'I':
		case 'O':
		case 'U':
			System.out.print(ch+" is a Vowel");
		break;
default:
System.out.print(ch+" is a Consonant");
	}

	}
}